    <h3 class="subtitle white">Visual Studio Code</h3>
    <a href="https://code.visualstudio.com/download">Télécharger VS Code</a>
</div>

<div class="bloc_content_img">

<div>
<p><span class="bold">Qu'est-ce que c'est ?</span><br>
Visual Studio Code, ou VS Code, est ce qu'on appelle un IDE, un environnement de 
développement intégré, c'est-à-dire un logiciel regroupant à minima un éditeur de 
code, des outils d'automatisation de la construction de code et un débogueur.<br>
En bref, c'est un logiciel de développement web qui va regrouper différents outils pour 
vous aider à coder.
</p>

<p>Il existe d'autres moyens de coder, car un simple éditeur de texte comme celui de 
votre système d'explotation peut faire l'affaire, ou bien certains éditeurs de texte 
pensés pour le développement comme SublimText, mais les IDE vous permette d'éccader à 
d'autres fonctionnalités bien utiles pour le développement.
</p>

<p><span class="bold">Pourquoi l'utiliser ?</span><br>
VS Code va par exemple vous permettre de découvrir et d'installer différentes 
extensions proposées par des entreprises ou des particulier, pour par exemple vous 
aider à avoir une meilleure syntaxe, vous avertir de beugs potentiels dans votre code 
ou ouvrir certains fichiers de manière plus ordonnée et lisible.<br>
Il est globalement fait pour s'adapter au mieux à votre usage, à vos habitudes de 
développement et aux langages que vous utilisez.<br>
On notera aussi que VS Code est un IDE gratuit et que sa grande popularité fait qu'il 
vous sera facile de trouver de l'aide ou des tutoriel en ligne pour l'utiliser au mieux 
et le customiser.
</p>
