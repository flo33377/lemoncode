<div class="bloc_content_img">

<div>
<p><span class="bold">Qu'est-ce que c'est ?</span><br>
JS Bin est un éditeur de code en ligne.<br>
Très utile pour vos débuts ou pour travailler rapidement sur un petit code front 
sans avoir à ouvrir d'éditeur de texte ou d'IDE, JS Bin vous permet de saisir du code 
HTML, CSS, JavaScript et d'avoir accès au rendu en direct ainsi qu'à une console 
(interface de débeugage).
</p>

<p><span class="bold">Pourquoi l'utiliser ?</span><br>
<ul>
    <li>Pour démarrer et vous entrainer aux langages HTML, CSS, JavaScript 
        car son rendu en direct vous permettra de bien comprendre l'impact de chaque 
        ligne de code et de vous familiariser avec ces langages ;
    </li>
    <li>Sur certains projets, pour tester en direct et gagner du temps. Par exemple lorsque 
        vous travaillerez sur des animations, les tester en direct sur JS Bin vous permettra 
        de modifier rapidement certains paramètres (le temps d'animation, des effets de 
        transformation, etc.) pour programmer votre animation idéale, puis la copier-coller dans 
        votre projet.
    </li>
</ul>
</p>
