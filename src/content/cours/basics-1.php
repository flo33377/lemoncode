<h2 class="subtitle green">Pourquoi démarrer par le fonctionnement du web ?</h2>

<p>Avant de savoir coder, il est essentiel de chercher à comprendre comment fonctionne le web.<br>
Comprendre comment fonctionne un serveur, ce qu'il se passe quand vous essayer d'accéder à une URL 
ou ce qu'est une erreur 404 vous permettra de mieux comprendre le web dans sa globalité et ainsi de pouvoir 
aller plus loin dans vos reflexions, et donc de coder mieux.</p>

<p>Personnellement, c'est un aspect que j'ai négligé au début de mon apprentissage, et quand je me suis renseigné 
plus tard dessus, cela m'a permis de mieux comprendre certaines erreurs que j'avais fait et des difficultés que j'avais 
rencontré jusqu'ici.
</p>

